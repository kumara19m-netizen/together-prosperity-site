export type LangCode = keyof typeof translations;
export type Translations = typeof translations[LangCode];
 
const LANG_KEY = 'tp_language';
 
const LANG_META: Record<LangCode, { label: string; native: string; flag: string; rtl?: boolean }> = {
  en: { label: 'EN',      native: 'English',    flag: '🇬🇧' },
  hi: { label: 'हिं',     native: 'हिन्दी',      flag: '🇮🇳' },
  kn: { label: 'ಕನ್ನಡ',   native: 'ಕನ್ನಡ',      flag: '🇮🇳' },
  ta: { label: 'தமிழ்',   native: 'தமிழ்',       flag: '🇮🇳' },
  te: { label: 'తెలుగు',  native: 'తెలుగు',      flag: '🇮🇳' },
  ml: { label: 'മലയ',    native: 'മലയാളം',      flag: '🇮🇳' },
  ar: { label: 'عربي',    native: 'العربية',      flag: '🇸🇦', rtl: true },
};
 
/* ─────────────────────────────── MODULE-LEVEL STORE ──────────────────────── */
let _lang: LangCode = 'en';
const _listeners = new Set<() => void>();
 
function _setLang(lang: LangCode) {
  _lang = lang;
  try {
    localStorage.setItem(LANG_KEY, lang);
    document.documentElement.lang = lang;
    document.documentElement.dir = LANG_META[lang].rtl ? 'rtl' : 'ltr';
  } catch {}
  _listeners.forEach(fn => fn());
}
 
function _loadSaved(): LangCode {
  try {
    const saved = localStorage.getItem(LANG_KEY) as LangCode;
    if (saved && saved in translations) return saved;
  } catch {}
  return 'en';
}
 
/* ─────────────────────────────── HOOK ──────────────────────────────────────── */
export function useLang(): { t: Translations; lang: LangCode; setLang: (l: LangCode) => void } {
  const [, forceUpdate] = useState(0);
 
  useEffect(() => {
    const saved = _loadSaved();
    if (saved !== _lang) {
      _lang = saved;
      document.documentElement.lang = saved;
      document.documentElement.dir = LANG_META[saved].rtl ? 'rtl' : 'ltr';
      forceUpdate(n => n + 1);
    }
    const rerender = () => forceUpdate(n => n + 1);
    _listeners.add(rerender);
    return () => { _listeners.delete(rerender); };
  }, []);
 
  return { t: translations[_lang], lang: _lang, setLang: _setLang };
}
 
