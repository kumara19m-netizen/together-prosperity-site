function SectionLabel({ children, borderTop }: { children: React.ReactNode; borderTop?: boolean }) {
  return (
    <div style={{
      padding: '8px 16px 4px',
      fontSize: '8px',
      letterSpacing: '3px',
      color: 'rgba(212,160,23,.4)',
      fontFamily: "'Space Mono', monospace",
      fontWeight: 700,
      borderTop: borderTop ? '1px solid rgba(255,255,255,.05)' : 'none',
      marginTop: borderTop ? '4px' : 0,
    }}>
      {children}
    </div>
  );
}
 
function LangOption({ l, current, onSelect }: {
  l: LangCode;
  current: LangCode;
  onSelect: (l: LangCode) => void;
}) {
  const meta = LANG_META[l];
  const active = current === l;
 
  return (
    <button
      onClick={() => onSelect(l)}
      style={{
        display: 'flex',
        alignItems: 'center',
        gap: '10px',
        width: '100%',
        padding: '10px 16px',
        background: active ? 'rgba(212,160,23,.12)' : 'transparent',
        border: 'none',
        color: active ? '#D4A017' : 'rgba(255,255,255,.62)',
        fontSize: '12.5px',
        fontWeight: active ? 700 : 400,
        cursor: 'pointer',
        textAlign: 'left',
        transition: 'all .16s',
      }}
      onMouseEnter={e => {
        if (!active) {
          (e.currentTarget as HTMLButtonElement).style.background = 'rgba(212,160,23,.07)';
          (e.currentTarget as HTMLButtonElement).style.color = '#D4A017';
        }
      }}
      onMouseLeave={e => {
        if (!active) {
          (e.currentTarget as HTMLButtonElement).style.background = 'transparent';
          (e.currentTarget as HTMLButtonElement).style.color = 'rgba(255,255,255,.62)';
        }
      }}
    >
      <span style={{ fontSize: '16px', lineHeight: 1, flexShrink: 0 }}>{meta.flag}</span>
      <span style={{ flex: 1 }}>{meta.native}</span>
      {active && <span style={{ fontSize: '11px', color: '#D4A017', flexShrink: 0 }}>✓</span>}
    </button>
  );
}